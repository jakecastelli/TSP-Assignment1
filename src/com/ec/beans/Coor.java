package com.ec.beans;

public class Coor {
    private Double x;
    private Double y;

    /**
     * Gets x coordinate
     * @return x
     */
    public Double getX() {
        return x;
    }

    /**
     * Sets x coordinate
     * @param x
     */
    public void setX(Double x) {
        this.x = x;
    }

    /**
     * Gets y coordinate
     * @return y
     */
    public Double getY() {
        return y;
    }

    /**
     * Sets y coordinate
     * @return y
     */
    public void setY(Double y) {
        this.y = y;
    }
}
